local Tunnel = module("vrp", "lib/Tunnel")
local Proxy = module("vrp", "lib/Proxy")
vRP = Proxy.getInterface("vRP")
-----------------------------------------------------------------------------------------------------------------------------------------
-- CONEX√ÉO
-----------------------------------------------------------------------------------------------------------------------------------------	
sRP = {}
Tunnel.bindInterface("mlx-showid",sRP)
vCLIENT = Tunnel.getInterface("mlx-showid")
-----------------------------------------------------------------------------------------------------------------------------------------
-- RETURN ID
-----------------------------------------------------------------------------------------------------------------------------------------	
function sRP.returnPlayers(source)
	local user_id = vRP.getUserId(source)
	return user_id
end
-----------------------------------------------------------------------------------------------------------------------------------------
-- CHECKPERMISSION
-----------------------------------------------------------------------------------------------------------------------------------------	
AddEventHandler("vRP:playerSpawn",function(user_id,source,first_spawn)
	if vRP.hasPermission(user_id,"admin.permissao") or vRP.hasPermission(user_id,"mod.permissao") or vRP.hasPermission(user_id,"id.permissao")  then
		vCLIENT.getPermission(source,true)
	end
end)

